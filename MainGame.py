def main():
    print("Простой калькулятор на Python")
    
    # Ввод двух чисел
    a = float(input("Введите первое число: "))
    b = float(input("Введите второе число: "))

    # Сложение
    result = a + b

    # Вывод результата
    print("Результат сложения:", result)

if __name__ == "__main__":
    main()
